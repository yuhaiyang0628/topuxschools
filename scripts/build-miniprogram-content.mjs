import { mkdir, readFile, writeFile } from "fs/promises";
import { dirname, resolve } from "path";
import { fileURLToPath } from "url";
import vm from "vm";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const contentDir = resolve(root, "content");
const outputDir = resolve(root, "miniprogram/data");
const seedDir = resolve(root, "miniprogram/seed");

async function loadWindowValue(fileName, key) {
  const source = await readFile(resolve(contentDir, fileName), "utf8");
  const context = { window: {} };
  vm.runInNewContext(source, context, { filename: fileName });
  return context.window[key];
}

async function writeJson(filePath, value) {
  await mkdir(dirname(filePath), { recursive: true });
  await writeFile(filePath, `${JSON.stringify(value, null, 2)}\n`, "utf8");
}

async function main() {
  const [programs, caseStudies, articles] = await Promise.all([
    loadWindowValue("programs.js", "PROGRAMS"),
    loadWindowValue("case-studies.js", "CASE_STUDIES"),
    loadWindowValue("articles.js", "ARTICLES")
  ]);

  await mkdir(outputDir, { recursive: true });
  await writeFile(
    resolve(outputDir, "content.js"),
    `// Generated by scripts/build-miniprogram-content.mjs. Do not edit manually.\nmodule.exports = ${JSON.stringify({ programs, caseStudies, articles }, null, 2)};\n`,
    "utf8"
  );

  await Promise.all([
    writeJson(resolve(seedDir, "programs.json"), programs),
    writeJson(resolve(seedDir, "caseStudies.json"), caseStudies),
    writeJson(resolve(seedDir, "articles.json"), articles)
  ]);

  console.log(`Synced ${programs.length} programs, ${caseStudies.length} cases, and ${articles.length} articles.`);
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
