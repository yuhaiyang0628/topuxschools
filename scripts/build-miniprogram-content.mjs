import { mkdir, readFile, writeFile } from "fs/promises";
import { dirname, resolve } from "path";
import { fileURLToPath } from "url";
import vm from "vm";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const contentDir = resolve(root, "content");
const outputDir = resolve(root, "miniprogram/data");
const seedDir = resolve(root, "miniprogram/seed");

async function loadContent() {
  const context = { window: {} };
  for (const fileName of ["programs.js", "case-studies.js", "articles.js"]) {
    const source = await readFile(resolve(contentDir, fileName), "utf8");
    vm.runInNewContext(source, context, { filename: fileName });
  }
  return {
    programs: context.window.PROGRAMS,
    caseStudies: context.window.CASE_STUDIES,
    articles: context.window.ARTICLES,
    caseTagCatalog: context.window.CASE_TAG_CATALOG || buildCaseTagCatalog(context.window.PROGRAMS, context.window.CASE_STUDIES)
  };
}

function buildCaseTagCatalog(programs, caseStudies) {
  const schools = new Map();
  const catalogPrograms = new Map();
  const addSchool = (item) => {
    if (!item || !item.school) return;
    if (!schools.has(item.school)) {
      schools.set(item.school, {
        id: item.school,
        label: item.label || item.school,
        school: item.school,
        schoolCn: item.schoolCn || "",
        aliases: [...new Set([item.label, item.school, item.schoolCn, ...(item.aliases || [])].filter(Boolean))]
      });
    }
  };
  programs.forEach((program) => {
    addSchool(program);
    catalogPrograms.set(program.id, {
      id: program.id,
      label: program.programShort || program.short || program.program,
      program: program.program,
      aliases: [...new Set([program.short, program.programShort, program.program].filter(Boolean))]
    });
  });
  caseStudies.forEach((caseStudy) => {
    (caseStudy.outcomes || []).forEach(addSchool);
    const selected = caseStudy.selected || {};
    if (selected.program && selected.program.label) {
      catalogPrograms.set(`case-${caseStudy.id}`, {
        id: `case-${caseStudy.id}`,
        label: selected.program.label,
        program: selected.program.program || selected.program.label,
        aliases: [...new Set([selected.program.label, selected.program.program, ...(selected.program.aliases || [])].filter(Boolean))]
      });
    }
  });
  return { schools: [...schools.values()], programs: [...catalogPrograms.values()] };
}

async function writeJsonLines(filePath, records) {
  await mkdir(dirname(filePath), { recursive: true });
  await writeFile(filePath, `${records.map((record) => JSON.stringify(record)).join("\n")}\n`, "utf8");
}

function prepareCollection(records, prefix) {
  return records.map((record) => ({
    ...record,
    _id: record._id || `${prefix}-${record.id}`,
    status: record.status || "published"
  }));
}

async function writeIndividualRecords(collectionName, records) {
  await Promise.all(records.map((record) => writeJsonLines(
    resolve(seedDir, collectionName, `${record._id}.json`),
    [record]
  )));
}

async function main() {
  const content = await loadContent();
  const programs = prepareCollection(content.programs, "program");
  const caseStudies = prepareCollection(content.caseStudies, "case");
  const articles = prepareCollection(content.articles, "article");
  const { caseTagCatalog } = content;

  await mkdir(outputDir, { recursive: true });
  await writeFile(
    resolve(outputDir, "content.js"),
    `// Generated by scripts/build-miniprogram-content.mjs. Do not edit manually.\nmodule.exports = ${JSON.stringify({ programs, caseStudies, articles, caseTagCatalog }, null, 2)};\n`,
    "utf8"
  );

  await Promise.all([
    writeJsonLines(resolve(seedDir, "programs.json"), programs),
    writeJsonLines(resolve(seedDir, "caseStudies.json"), caseStudies),
    writeJsonLines(resolve(seedDir, "articles.json"), articles),
    writeIndividualRecords("programs", programs),
    writeIndividualRecords("caseStudies", caseStudies),
    writeIndividualRecords("articles", articles)
  ]);

  console.log(`Synced ${programs.length} programs, ${caseStudies.length} cases, and ${articles.length} articles with full and individual seed files.`);
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
